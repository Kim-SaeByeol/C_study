#include <stdio.h>
	void main() {
	printf("입력 진수 결정 <1> 10, <2> 16, <3> 8 : ");
	int n,m;
	scanf ("%d", &n);
	if(n == 1) {
	printf("값 입력 : ");
	scanf("%d", &m); 
	printf("10진수 ==> %d\n", m); 
	printf("16진수 ==> %x\n", m);
	printf("8진수 ==> %o\n", m);
	}
	else if(n == 2) {
	printf("값 입력 : ");
	scanf("%x", &m);
	printf("10진수 ==> %d\n", m);
	printf("16진수 ==> %x\n", m);
	printf("8진수 ==> %o\n",m);
	}
	else if(n == 3) {
	printf("값 입력 : ");
	scanf("%o", &m);
	printf("10진수 ==> %d\n", m);
	printf("16진수 ==> %x\n", m);
	printf("8진수 ==> %o\n", m);
	}
	else {
	printf("오류 다시 입력해주세요. 입력 진수 결정 <1> 10, <2> 16, <3> 8");
	}
	}
